    def system_history(self, args) -> int:
        """Show deployment history"""
        try:
            conn = _get_conn()
            limit = getattr(args, 'limit', 10)
            project_filter = getattr(args, 'project', None)
            query = "SELECT * FROM system_deployments"
            params = []
            if project_filter:
                query += " WHERE project_id = (SELECT id FROM projects WHERE slug = ?)"
                params.append(project_filter)
            query += " ORDER BY deployed_at DESC LIMIT ?"
            params.append(limit)
            rows = conn.execute(query, params).fetchall()
            if not rows:
                print("No deployment history found.")
                return 0
            for row in rows:
                print(f"  [{row[0]}] {row[-1]} — exit {row[-2]}")
            return 0
        except Exception as e:
            logger.error(f"Failed to get history: {e}", exc_info=True)
            return 1

